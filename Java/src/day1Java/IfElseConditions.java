package day1Java;

public class IfElseConditions {

	public static void main(String[] args) {
		
		int x=18;
		if(true) {
			System.out.println("hi");
		}
		if(false) {
			System.out.println("hi");
		}
		
		if(x>10) {
			System.out.println("is is greater than 10");
		}
		
		if(x>10&&x<3) {
			System.out.println("it is satisfying the give condition");
		}
		else {
			System.out.println("It is false");
		}
		
		
	}
}
